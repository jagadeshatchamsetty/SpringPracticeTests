public class Address {
	
	private String street;
	private String homeNo;
	private String state;
	private int pin;
	public Address() {
		
	}
	public String getStreet() {
		return street;
	}
	public String getHomeNo() {
		return homeNo;
	}
	public String getState() {
		return state;
	}
	public int getPin() {
		return pin;
	}
	

}
